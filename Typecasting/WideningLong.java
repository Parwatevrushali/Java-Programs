class WideningLong
{
	public static void main(String[] args) 
	{


		  // Widening conversion:  long  to  float , double

		  long l1= 4323456l;
		  System.out.println(l1); //4323456

		  float f1 = l1;
		  System.out.println(f1); //4323456.0

		  double d1 = f1;
		  System.out.println(d1); //4323456.0  

	}

}