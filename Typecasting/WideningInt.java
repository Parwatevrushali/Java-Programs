class WideningInt
{
	public static void main(String[] args) 
	{


		 // Widening conversion:  int  to  long , float , double

		 int i1= 100000;
		 System.out.println(i1);  //100000

		 long l1 = i1;
		 System.out.println(l1);  //100000

		 float f1 = l1;
		 System.out.println(f1);  //100000.0

		 double d1 = f1;
		 System.out.println(d1);  //100000.0   

	}

}
