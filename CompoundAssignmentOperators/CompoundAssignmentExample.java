class CompoundAssignmentExample 
{
	public static void main(String[] args) 
	{
		int sal =1000;
		sal+=5000;
		System.out.println(sal);

		int poc = 90000;
		poc-=5000;
		System.out.println(poc);

		char ch = 'A';
		ch += 32;
		System.out.println(ch);

		char ch1 = 'a';
		ch1 -=  32;
		System.out.println(ch1);

		int balance = 78000;
		balance += 2000;
		System.out.println(balance);
	}
}
