class RelationalDivisionPractice {
    public static void main(String[] args) {

        // Example 1
        System.out.println(15 / 4 == 15.0 / 4);  // false

        // Example 2
        System.out.println('B' + 1 == 'C');  // true

        // Example 3
        System.out.println(5 / 2.0 > 5 / 2); // true

        // Example 4
        System.out.println(10.0f / 3 == 10.0 / 3); // false

        // Example 5
        System.out.println('a' + 1 > 'b'); // false

        // Example 6
        System.out.println((float)(7 / 2) == 3.5f); // false

        // Example 7
        System.out.println(9 / 3 == 3.0); // true

        
    }
}
